    public void populateTable()
    {
        DefaultTableModel defaultTableModel = (DefaultTableModel)airlinerListJTable.getModel();
        defaultTableModel.setRowCount(0);
        
        for(Airliner airliner : travelAgency.getAirlinerCatalog())
        {
            Object row[] = new Object[3];
            row[0] = airliner;
            row[1] = airliner.getAirlinerOwner();
            row[2] = airliner.totalAirplane(airliner.getAirplaneCatalog());
            defaultTableModel.addRow(row);
        }
    }

