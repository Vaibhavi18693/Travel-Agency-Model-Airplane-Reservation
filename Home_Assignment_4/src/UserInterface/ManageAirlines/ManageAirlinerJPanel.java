    private void viewAirlinerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewAirlinerBtnActionPerformed
        
        int selectedRow = airlinerListJTable.getSelectedRow();
        if(selectedRow < 0)
        {
            JOptionPane.showMessageDialog(null,"Please select row from table to view details","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            Airliner airliner = (Airliner)airlinerListJTable.getValueAt(selectedRow,0);
            ViewAirlinerJPanel viewAirlinerJPanel = new ViewAirlinerJPanel(userProcessContainer,airliner);
            userProcessContainer.add("ViewAirlinerJPanel",viewAirlinerJPanel);
            CardLayout layout = (CardLayout)userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_viewAirlinerBtnActionPerformed

    private void manageAirplaneBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_manageAirplaneBtnActionPerformed
        
        int selectedRow = airlinerListJTable.getSelectedRow();
        
        if(selectedRow < 0)
        {
            JOptionPane.showMessageDialog(null,"Please select airliner from table to manage airplane details","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            Airliner airliner = (Airliner)airlinerListJTable.getValueAt(selectedRow, 0);
            ManageAirplanesJPanel manageAirplanesJPanel = new ManageAirplanesJPanel(userProcessContainer, airliner);
            userProcessContainer.add("ManageAirplanesJPanel",manageAirplanesJPanel);
            CardLayout layout = (CardLayout)userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_manageAirplaneBtnActionPerformed

    private void backBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backBtnActionPerformed
        
        userProcessContainer.remove(this);
        Component[] componentArray = userProcessContainer.getComponents();
        Component component = componentArray[componentArray.length-1];
        TravelAgencyJPanel travelAgencyJPanel = (TravelAgencyJPanel)component;
        travelAgencyJPanel.populateTable();
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_backBtnActionPerformed

    private void deleteAirlinerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteAirlinerBtnActionPerformed
        
        int selectedRow = airlinerListJTable.getSelectedRow();
        if(selectedRow < 0)
        {
            JOptionPane.showMessageDialog(null,"Please select row from table to delete airplane","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            Airliner airliner = (Airliner)airlinerListJTable.getValueAt(selectedRow, 0);
            travelAgency.deleteAirliner(airliner);
            populateTable();
        }
    }//GEN-LAST:event_deleteAirlinerBtnActionPerformed


