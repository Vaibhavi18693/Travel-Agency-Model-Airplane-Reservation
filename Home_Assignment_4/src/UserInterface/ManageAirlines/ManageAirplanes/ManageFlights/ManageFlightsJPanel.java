    private void viewFlightsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewFlightsBtnActionPerformed
        
        int selectedRow = flightScheduleJTable.getSelectedRow();
        if(selectedRow < 0)
        {
            JOptionPane.showMessageDialog(null,"Please select row from table to view details","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            Flights flight = (Flights)flightScheduleJTable.getValueAt(selectedRow, 0);
            ViewFlightsJPanel viewFlightsJPanel = new ViewFlightsJPanel(userProcessContainer,flight);
            userProcessContainer.add("ViewFlightsJPanel",viewFlightsJPanel);
            CardLayout layout = (CardLayout)userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }  
    }//GEN-LAST:event_viewFlightsBtnActionPerformed

    private void backBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backBtnActionPerformed
        
        userProcessContainer.remove(this);
        Component[] componentArray = userProcessContainer.getComponents();
        Component component = componentArray[componentArray.length-1];
        ManageAirplanesJPanel manageAirplanesJPanel = (ManageAirplanesJPanel)component;
        manageAirplanesJPanel.populateTable();
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_backBtnActionPerformed

    private void cancelFlightBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelFlightBtnActionPerformed
        
        int selectedRow = flightScheduleJTable.getSelectedRow();
        if(selectedRow < 0)
        {
            JOptionPane.showMessageDialog(null,"Please select row from table to cancel flight.","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            Flights flight = (Flights)flightScheduleJTable.getValueAt(selectedRow, 0);
            airplane.deleteFlight(flight);
            populateTable();
        }
    }//GEN-LAST:event_cancelFlightBtnActionPerformed


