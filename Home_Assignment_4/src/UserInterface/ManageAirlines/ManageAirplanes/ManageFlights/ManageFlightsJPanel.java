    public void populateTable()
    {
        DefaultTableModel defaultTableModel = (DefaultTableModel)flightScheduleJTable.getModel();
        defaultTableModel.setRowCount(0);
        
        for(Flights flight : airplane.getFlightCatalog())
         {
            Object row[] = new Object[5];
            row[0] = flight;
            row[1] = flight.getSource();
            row[2] = flight.getDestination();
            row[3] = flight.getFlightDate();
            row[4] = flight.getFlightTime();
            defaultTableModel.addRow(row);
        }
    }
